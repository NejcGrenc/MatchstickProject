package grenc.masters.webpage.translations;

public class Translation
{
	private String en;
	private String si;
	private String de;
	private String sk;
	
	
	public String getEn()
	{
		return en;
	}
	public void setEn(String en)
	{
		this.en = en;
	}
	public String getSi()
	{
		return si;
	}
	public void setSi(String si)
	{
		this.si = si;
	}
	public String getDe()
	{
		return de;
	}
	public void setDe(String de)
	{
		this.de = de;
	}
	public String getSk()
	{
		return sk;
	}
	public void setSk(String sk)
	{
		this.sk = sk;
	}

}
